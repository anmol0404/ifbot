import env from "../services/env.js";

function sendRequest() {
  fetch(env.otherDomain)
    .then((response) => {
      if (!response.ok) {
        throw new Error("Network response was not ok");
      }
      return response.json();
    })
    .then((data) => {
      console.log(data);
    })
    .catch((error) => {
      console.error("Error:", error);
    });
}
export default sendRequest;
